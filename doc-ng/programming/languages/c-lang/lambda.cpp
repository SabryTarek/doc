#define watch(x) cout << (x) << '\n'
#define print(x) cout << (x) << ' '
/*************************************************************************************************************************/
#include <iostream>
#include <vector>
#include <algorithm>
/*************************************************************************************************************************/
using namespace std;
/*************************************************************************************************************************/
template <typename T>
auto gemy = [](auto min = 0, auto max = 0) {
    vector<T> v;

    for (; min < max+1; min++)
    {
        v.push_back(min);
    }

    return v;
};
/*************************************************************************************************************************/
int main()
{

    vector<int> v2 = gemy<int>(0, 4);

    for (auto i : v2)
    {
        watch(i);
    }

    return 0;
}