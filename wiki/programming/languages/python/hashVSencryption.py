import hashlib

# Hashing MD5
H = hashlib.md5(b"Update").hexdigest()
print("MD5 =",H)
print("Size =",len(H))



# Hashing SHA1
H = hashlib.sha1(b"Update").hexdigest()
print("SHA1 =",H)
print("Size =",len(H))



# Hashing SHA224
H = hashlib.sha224(b"Update").hexdigest()
print("SHA224 =",H)
print("Size =",len(H))



# Hashing SHA256
H = hashlib.sha256(b"Update").hexdigest()
print("SHA256 =",H)
print("Size =",len(H))



# Hashing SHA384
H = hashlib.sha384(b"Update").hexdigest()
print("SHA384 =",H)
print("Size =",len(H))



# Hashing SHA512
H = hashlib.sha512(b"Update").hexdigest()
print("SHA512 =",H)
print("Size =",len(H))



